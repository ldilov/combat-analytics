# Preferred field ordering for spell intelligence entries
SPELL_FIELD_ORDER = [
    "category", "isMajorOffensive", "isMajorDefensive", "isImmunity",
    "isTrinketLike", "isPvPTrinket", "isBurstEnabler", "breaksCC",
    "isRecoveryTool", "isInterrupt", "interruptFamily",
    "isCrowdControlStarter", "ccFamily", "isMobility",
    "notesTag", "cooldownSeconds",
]


def _spell_inline(entry: dict) -> str:
    parts = []
    for field in SPELL_FIELD_ORDER:
        if field not in entry:
            continue
        parts.append(f"{field} = {_lua_val(entry[field])}")
    # any extra fields not in the preferred order
    for field, val in entry.items():
        if field in SPELL_FIELD_ORDER or field.startswith("_") or field == "spellID":
            continue
        parts.append(f"{field} = {_lua_val(val)}")
    return "{ " + ", ".join(parts) + " }"


def write_spell_intelligence(ts: str) -> None:
    src = RAW_DIR / "spell_intelligence_overrides.json"
    if not src.exists():
        print(f"[skip] {src} not found")
        return

    data = json.loads(src.read_text(encoding="utf-8"))
    raw_spells = data.get("spells", [])

    out = SEED_DIR / "SeedSpellIntelligence.lua"
    lines = [HEADER.format(ts=ts)]
    lines.append("ns.GeneratedSeedData.spellIntelligence = {")
    lines.append("")

    spell_count = 0
    current_top_section: str | None = None
    current_sub_section: str | None = None

    for entry in raw_spells:
        # Section marker
        if "_section" in entry:
            sec = entry["_section"]
            top_title = SECTION_TITLES.get(sec)
            sub_label  = SUBSECTION_LABELS.get(sec)

            if top_title and top_title != current_top_section:
                # New top-level section header
                current_top_section = top_title
                current_sub_section = None
                sep = "=" * 57
                lines.append(f"    -- {sep}")
                lines.append(f"    -- {top_title}")
                lines.append(f"    -- {sep}")

            if sub_label and sub_label != current_sub_section:
                current_sub_section = sub_label
                lines.append(f"")
                lines.append(f"    -- {sub_label}")

            continue

        spell_id = entry.get("spellID")
        if spell_id is None:
            continue

        spell_count += 1
        inline = _spell_inline(entry)
        # Pad the spell ID column for alignment (up to 6 digits)
        id_str = f"[{spell_id}]"
        lines.append(f"    {id_str:<8} = {inline},")

    lines.append("")
    lines.append("}")
    lines.append("")
    out.write_text("\n".join(lines), encoding="utf-8")
    print(f"[ok] Wrote {out}  ({spell_count} spells)")


