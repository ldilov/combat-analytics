ARCHETYPE_FIELD_ORDER = [
    "classFile", "specName", "role", "rangeBucket", "archetype", "threatTags",
]


def write_spec_archetypes(ts: str) -> None:
    src = RAW_DIR / "spec_archetypes.json"
    if not src.exists():
        print(f"[skip] {src} not found")
        return

    data = json.loads(src.read_text(encoding="utf-8"))
    specs = data.get("specs", [])

    out = SEED_DIR / "SeedSpecArchetypes.lua"
    lines = [HEADER.format(ts=ts)]
    lines.append("ns.GeneratedSeedData.specArchetypes = {")

    for s in sorted(specs, key=lambda x: x["specId"]):
        spec_id = s["specId"]
        # Build the inline table preserving field order
        parts = []
        for field in ARCHETYPE_FIELD_ORDER:
            if field not in s:
                continue
            parts.append(f"{field} = {_lua_val(s[field])}")
        lines.append(f"    [{spec_id}] = {{ {', '.join(parts)} }},")

    lines.append("}")
    lines.append("")
    out.write_text("\n".join(lines), encoding="utf-8")
    print(f"[ok] Wrote {out}  ({len(specs)} specs)")


