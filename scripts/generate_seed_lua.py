SPEC_META_FIELD_ORDER = [
    "specId", "specName", "className", "classId", "classFile", "role", "iconFileDataId",
]


def write_spec_meta_from_cache(ts: str) -> None:
    src = RAW_DIR / "bnet_spec_meta.json"
    if not src.exists():
        print(f"[skip] {src} not found — skipping SeedSpecMeta.lua (run fetch_bnet_seed.py first)")
        return

    raw = json.loads(src.read_text(encoding="utf-8"))
    # Keys may be strings (from JSON) — normalise to int
    spec_meta: dict[int, dict] = {int(k): v for k, v in raw.items()}

    out = SEED_DIR / "SeedSpecMeta.lua"
    lines = [HEADER.format(ts=ts)]
    lines.append("--- Official spec metadata sourced from the Blizzard Game Data API.")
    lines.append("--- @field specId         number")
    lines.append("--- @field specName       string")
    lines.append("--- @field className      string")
    lines.append("--- @field classId        number")
    lines.append("--- @field classFile      string  e.g. DEATHKNIGHT, DRUID …")
    lines.append("--- @field role           string  DAMAGE | HEALER | TANK")
    lines.append("--- @field iconFileDataId number|nil  WoW internal texture ID")
    lines.append("ns.GeneratedSeedData.specMeta = {")

    for spec_id in sorted(spec_meta.keys()):
        m = spec_meta[spec_id]
        icon = str(m["iconFileDataId"]) if m.get("iconFileDataId") else "nil"
        lines.append(f"    [{spec_id}] = {{")
        lines.append(f"        specId        = {spec_id},")
        lines.append(f"        specName      = {_esc(str(m.get('specName', '')))},")
        lines.append(f"        className     = {_esc(str(m.get('className', '')))},")
        lines.append(f"        classId       = {m.get('classId', 0)},")
        lines.append(f"        classFile     = {_esc(str(m.get('classFile', '')))},")
        lines.append(f"        role          = {_esc(str(m.get('role', 'DAMAGE')))},")
        lines.append(f"        iconFileDataId = {icon},")
        lines.append("    },")

    lines.append("}")
    lines.append("")
    out.write_text("\n".join(lines), encoding="utf-8")
    print(f"[ok] Wrote {out}  ({len(spec_meta)} specs)")


