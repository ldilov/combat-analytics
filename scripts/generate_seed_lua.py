# ---------------------------------------------------------------------------
BOOL_FLAGS_DUMMY = {
    "isPvPUseful", "isRaidDummy", "isDungeonDummy", "isTrainingGrounds",
}

DUMMY_FIELD_ORDER = [
    "creatureID", "displayName", "normalizedName", "family",
    "benchmarkGroup", "isPvPUseful", "isRaidDummy", "isDungeonDummy", "isTrainingGrounds",
]


def write_dummy_catalog(ts: str) -> None:
    src = RAW_DIR / "dummy_catalog_overrides.json"
    if not src.exists():
        print(f"[skip] {src} not found")
        return

    data = json.loads(src.read_text(encoding="utf-8"))
    dummies = data.get("dummies", [])

    out = SEED_DIR / "SeedDummyCatalog.lua"
    lines = [HEADER.format(ts=ts)]
    lines.append("ns.GeneratedSeedData.dummyCatalog = {")

    for d in sorted(dummies, key=lambda x: x["creatureID"]):
        cid = d["creatureID"]
        lines.append(f"    [{cid}] = {{")
        for field in DUMMY_FIELD_ORDER:
            if field not in d:
                continue
            lines.append(f"        {field} = {_lua_val(d[field])},")
        lines.append("    },")

    lines.append("}")
    lines.append("")
    out.write_text("\n".join(lines), encoding="utf-8")
    print(f"[ok] Wrote {out}  ({len(dummies)} dummies)")


