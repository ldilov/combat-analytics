# SeedPvpTalents.lua  (from cached bnet_pvp_talents.json)
# ---------------------------------------------------------------------------

def write_pvp_talents_from_cache(ts: str) -> None:
    src = RAW_DIR / "bnet_pvp_talents.json"
    if not src.exists():
        print(f"[skip] {src} not found — skipping SeedPvpTalents.lua (run fetch_bnet_seed.py first)")
        return

    raw = json.loads(src.read_text(encoding="utf-8"))
    by_spec: dict[int, list[dict]] = {int(k): v for k, v in raw.items()}

    # Load spec meta for class name comments (optional)
    spec_meta: dict[int, dict] = {}
    meta_src = RAW_DIR / "bnet_spec_meta.json"
    if meta_src.exists():
        spec_meta = {int(k): v for k, v in json.loads(meta_src.read_text()).items()}

    out = SEED_DIR / "SeedPvpTalents.lua"
    lines = [HEADER.format(ts=ts)]
    lines.append("--- PvP talents per spec, sourced from the Blizzard Game Data API.")
    lines.append("--- Each entry: { talentId, spellId, name, description, castTime, slots }.")
    lines.append("--- spellId can be used with C_Spell.GetSpellInfo(spellId) for icon lookup.")
    lines.append("--- slots: compatible PvP talent slot numbers (usually 1-4).")
    lines.append("ns.GeneratedSeedData.pvpTalents = {")

    total_talents = 0
    for spec_id in sorted(by_spec.keys()):
        talents = by_spec[spec_id]
        meta    = spec_meta.get(spec_id, {})
        comment = f"{meta.get('specName', '?')} {meta.get('className', '?')}"
        lines.append(f"    [{spec_id}] = {{  -- {comment}")

        for t in talents:
            spell_id  = t.get("spellId") or "nil"
            talent_id = t.get("talentId") or "nil"
            name      = _esc(t.get("name", ""))
            desc      = _esc(t.get("description", ""))
            cast_time = _esc(t.get("castTime", ""))
            slots_raw = t.get("compatibleSlots") or t.get("slots") or []
            slots_str = "{ " + ", ".join(str(s) for s in slots_raw) + " }" if slots_raw else "{}"
            lines.append(f"        {{ talentId = {talent_id}, spellId = {spell_id}, name = {name},")
            lines.append(f"          description = {desc},")
            lines.append(f"          castTime = {cast_time}, slots = {slots_str} }},")
            total_talents += 1

        lines.append("    },")

    lines.append("}")
    lines.append("")
    out.write_text("\n".join(lines), encoding="utf-8")
    print(f"[ok] Wrote {out}  ({len(by_spec)} specs, {total_talents} talents)")


